package myapplication.pages.CheckUps;

public class Checkup {
    private int id;
    private String name;

    public Checkup(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}

